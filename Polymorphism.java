package Program;

public class Polymorphism {

	public static void main(String[] args) {
		System.out.println(num());
		System.out.println(sum());
	}
    public static int num() {
	int a=56;
    return a;
	}
	public static int sum() {
		int a= 56;
		int b= 40;
		return a+b;
	}
	
}
 